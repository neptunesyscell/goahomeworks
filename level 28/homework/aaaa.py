# ფუნქცია არის კოდის ბლოკი, რომელსაც აქვს სახელი და შესრულების კონკრეტული მიზანი.
# ფუნქციები საშუალებას გვაძლევს კოდი გავხადოთ უფრო ორგანიზებული, მრავალჯერ გამოსაყენებელი და ადვილად გასაგები.

# ფუნქცია double იღებს ერთ რიცხვს და აბრუნებს მას გაორმაგებულს.
def double(number):
    """
    ფუნქცია, რომელიც აბრუნებს გადაცემული რიცხვის გაორმაგებულს.
    :param number: რიცხვი, რომელიც უნდა გავაორმაგოთ
    :return: გაორმაგებული რიცხვი
    """
    return number * 2

# მაგალითი გამოყენებისთვის
print(double(5))  # 10

# თქვენი ფუნქციები:
# ფუნქცია, რომელიც ამოწმებს რიცხვი კენტია თუ ლუწი
def is_odd(number):
    """
    ფუნქცია, რომელიც ამოწმებს რიცხვი კენტია თუ არა.
    :param number: შესამოწმებელი რიცხვი
    :return: True თუ კენტია, False - თუ არა
    """
    return number % 2 != 0

# მაგალითი
print(is_odd(5))  # True
print(is_odd(8))  # False

# ფუნქცია, რომელიც აბრუნებს სიტყვების რაოდენობას ტექსტში
def word_count(text):
    """
    ფუნქცია, რომელიც ითვლის სიტყვების რაოდენობას ტექსტში.
    :param text: ტექსტი
    :return: სიტყვების რაოდენობა
    """
    return len(text.split())

# მაგალითი
print(word_count("ეს არის მარტივი ტექსტი"))  # 4
